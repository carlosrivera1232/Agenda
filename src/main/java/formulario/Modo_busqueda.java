    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
        String nombre = jTextField1.getText();
        int comprobacion=0;
        for (int i = 0; i < inicio.agenda.size(); i++) {
            eventos c = (eventos) inicio.agenda.get(i);
            //String comparacion = c.getFecha(i);
            if(nombre.equals(c.getEvento())){
                Eventounico abrir = new Eventounico();
                num=i;
                comprobacion =1;
                JOptionPane.showMessageDialog(null,"Se encontro el evento");
                abrir.setVisible(true);
                this.setVisible(false);
                i=inicio.agenda.size();
                
            }
         }
        if(comprobacion==0){
            JOptionPane.showMessageDialog(null, "No se encontro evento");
        }
    }//GEN-LAST:event_jRadioButton2ActionPerformed

